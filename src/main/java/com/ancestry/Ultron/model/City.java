package com.ancestry.Ultron.model;

public class City {
	Long id;
	String name;
	Long state_id;

	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Long getStateId() {
		return state_id;
	}
	public void setStateId(Long state_id) {
		this.state_id = state_id;
	}
	
}
